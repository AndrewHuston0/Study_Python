#세트: 연산

nums_1 = {1,3,5}
nums_2 = {5,7,9}

print(nums_1 & nums_2) # 두 개의 교집합을 의미함
print(nums_1 | nums_2) # 두 개의 합집합을 의미함
print(nums_1 - nums_2) # 차집합으로 nums_1에는 있지만 nums_2에는 없는 것

# 따라서 그냥 수학의 집합이라고 이해하면 편함
